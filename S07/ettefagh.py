def calculate_duration(data):
    #       f(i)  <--i-->   <------iterable-------->
    return [b-a for a, b in zip(data[:-1], data[1:])]

def tag(i, t1=300, t2=500):
    if i < t1:
        tmp = "S"
    elif i > t2:
        tmp = "L"
    else:
        tmp = "M"
    return tmp


